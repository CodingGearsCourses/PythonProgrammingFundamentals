# Tuples
# A tuple is an immutable sequence of Python objects.

# Create
tuple_1 = (1, 2, 3, 4, 5)
tuple_2 = (1, "Hello")

# Using + operator
print(" Using + operator ".center(30, "-"))
tuple_3 = tuple_1 + tuple_2
print(tuple_3)

# Using * operator
print(" Using * operator ".center(30, "-"))
print(tuple_2 * 2)
